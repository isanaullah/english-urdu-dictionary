<h1>US Immigration</h1>
United States of America is one the richest and strongest country in the world and therefore needs no introduction. That's the reason that people from all over the world seek its immigration. In this article we are going to cover all aspects of US immigration. US offers virous types of immigration, tailored for specific needs of the applicant. The article will also provide answers on how to get USA immigration, how to apply, the whole procedure and the time it will take.
<h2>Types of US Immigration</h2>
<ol>
 	<li>Family Based Immigration</li>
 	<li>Employment Based Immigration (H-1B, L-1, O-1. E-2)</li>
 	<li>Permanent Based Employment Immigration Green Card (EB-1, EB-2, EB-3, EB-4, EB- 5)</li>
 	<li>Diversity Visa (DV) Lottery</li>
 	<li>Humanitarian Immigration (Asylum, Refugee Status, TPS, Humanitarian Parole)</li>
 	<li>Student and Exchange Visitor Visa (F-1, J-1, M-1)</li>
 	<li>Special Immigrant Categories (SIJ, SIVs)</li>
 	<li>Neutralization</li>
</ol>
<h3>Family Based immigration</h3>
The citizens of United States of America are allowed to sponsor their spouses,, children (under 21), parents and siblings. So if you have anyone in your family who is US citizen you can avail this immigration type.
<h3>Employment Based Immigration</h3>
This type includes individuals with temporary work visas:
<ul>
 	<li><strong>H-1B Visa:</strong> This includes individuals who fits in the category of skilled worker (specialist in occupation). This category normally acquires at least a bachelor's degree.</li>
 	<li><strong>L-1 Visa:</strong> For individuals who having specialization in their field or working in administrative or executive roles.</li>
 	<li><strong>O-1 visa:</strong> This visa type includes individuals who have expertise and extraordinary abilities in the field of science, arts, education, business or athletics.</li>
 	<li><strong>E-2Visa:</strong> This is for the individuals who happen to reside in country that are in treaty with US and are looking to invest a substantial sum in US.</li>
</ul>
<h3>Permanent Based Employment Immigration Green Card</h3>
<ul>
 	<li><strong>EB- 1:</strong> Professors, researchers, multinational managers and executives can use this visa type. They have to extraordinary in their relevant fields.</li>
 	<li><strong>EB-2:</strong> Individuals with advanced degrees or extraordinary skills in the relevant fields can use this visa type.</li>
 	<li><strong>EB-3:</strong> Professionals, skilled workers and other workers can apply in this category.</li>
 	<li><strong>EB-4:</strong> Religious workers, special immigrants and specific employees from international organizations can apply in this category.</li>
 	<li><strong>EB-5:</strong> For individuals who are looking to invest a large sum in US, large enough to create jobs for US citizens.</li>
</ul>
<h3>Diversity Visa (DV) Lottery</h3>
Diversity immigration visa program is largely known as the green card lottery. This programs randomly selects 55000 individuals annually and from the countries that have low US immigration rates.
<h3>Humanitarian Immigration</h3>
<ul>
 	<li><strong>Asylum: </strong>For individuals who are already in US and face serious threats going back home based on , religion, political opinion, nationality or race.</li>
 	<li><strong>Refugee Status:</strong> This status is quite similar Asylum, but includes individuals who are not in their home countries but face persecution in their country.</li>
 	<li><strong>Temporary Protected Status (TPS):</strong> This types offers provides special status (temporary legal status) to individuals who are facing environmental disasters, armed conflict and any other hazardous conditions in their home country.</li>
 	<li><strong>Humanitarian Parole: </strong>Allows individuals to enter the U.S. temporarily for urgent humanitarian reasons or significant public benefit.</li>
</ul>
<h3>Student and Exchange Visitor Visa</h3>
<ul>
 	<li><strong>F-1 Visa:</strong> For individuals who are attending academic or English language programs in US.</li>
 	<li><strong>M-1 Visa:</strong> This visa types is for the students who are attending vocational or non-academic programs.</li>
 	<li><strong>J-1 Visa:</strong> This visa types includes individuals involved in exchange visitors participating in work and study based exchange programs; including (Pairs, interns, and researchers).</li>
</ul>
<h3>Special Immigrant Categories</h3>
<ul>
 	<li><strong>Special Immigrant Juveniles (SIJ):</strong> This category includes minors who have been neglected, abused, abandoned by parents/ parent.</li>
 	<li><strong>Special Immigrant Visas (SIVs):</strong> This category is for individuals who have in supporting US army or Government in Countries like Iraq and Afghanistan (Either as a translator or and interpreter).</li>
</ul>
<h3>Neutralization</h3>
This category helps individuals who are green card holders, which is obtained by 5 years of permanent residency or after 3 years of marriage with a US citizen.

Now as we have mentioned each of the immigration type, your first step should be to select the one the suits your status the most.
<h3>Determining Eligibility</h3>
This is the first step to a successful US immigration is to select the appropriate category for yourself. This is the most important step because most of the people select the wrong immigration category, resulting in rejection of the application. Once you have selected the right category for yourself the next step is to check the requirements and eligibilities of that specific category. As there are so many different requirements which we couldn't provide in as single article, so we are providing you with the link of the official website of US immigration https://www.uscis.gov/.
<h3>Filing Petition</h3>
Once you are done with the eligibility check the next and the most crucial step is to file the petition of US immigration. The petition is basically a formal request to US home department to recognize your ability and start the process of immigration. Below is given the details on how to filed the petition:
<ul>
 	<li>Complete Form I-130</li>
 	<li>Submit Supporting Documents</li>
 	<li>Pay the Filing Fee</li>
 	<li>Mail the Petition</li>
 	<li>Receipt Notice</li>
 	<li>Processing and Approval</li>
</ul>
The easiest and the most easy way to do this process is to seek professional help. You can always look for assistance from law attorneys and law firm who specialize in immigration and visa services. They will be beneficial for you in other things to like, guidance in where and how to get settled in US, the funds that you will require and more. One can find them online and if you need any assistance on finding the immigration experts, leave a comment, we will assist you with another article on you quires.

The is another common quire about US immigration that you might have in mind too. How long does it take to get US immigration, or the US immigration processing time? So, lets answer it and put you mind at ease. The US immigration process time depends upon the category or type you are applying on. The processing time varies from type to type, for some immigration it takes 1 to 2 years but for some it could take up to 10 to 15 years. So, it really depends on you category status, but one you have filed the petition you will get the estimated date form the visa officers.

